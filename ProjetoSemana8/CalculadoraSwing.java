    private void tratarNumeroPressionado(String numero){
     if(digitou == false){
         jLabel1.setText(numero);
         digitou = true;
     }else{
         String visor =jLabel1.getText();
         jLabel1.setText(visor+numero);
     }   
    }
    private void tratarOperacaoPressionada(int operacaoDesejada){
        operacao = operacaoDesejada;
        numeroDigitado = Integer.parseInt (jLabel1.getText());
        digitou = false;
    }
    private void tratarIgualPressoinado(){
        switch(operacao){
            case 1: //operaçaõ de soma
                total = numeroDigitado + Integer.parseInt (jLabel1.getText());
                break;
            case 2: // subrtração
                total = numeroDigitado - Integer.parseInt (jLabel1.getText());
                break;
            case 3: //divisão,
                total = numeroDigitado / Integer.parseInt (jLabel1.getText());
                break;
            case 4: //mutiplicação
                total = numeroDigitado * Integer.parseInt(jLabel1.getText());
                break;
        }
        jLabel1.setText(Integer.toString(total));
    }
    
    
    
